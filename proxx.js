// اختيار المصفوفة بناءً على الصفحة المفتوحة
let products = [];
const currentPage = window.location.pathname;

if (currentPage.includes("features.html")) {
  products = productsPage1;
} else if (currentPage.includes("betuflpage.html")) {
  products = productsPage2;
} else if (currentPage.includes("hmtls.html")) {
  products = productsPage3;
} else {
  products = productsPage1;
}
// Pagination Variables
let currentProductPage = 1;
const itemsPerPage = 16;
const totalPages = Math.ceil(products.length / itemsPerPage);

// Function to display cards based on page number
function displayCards(page) {
  const start = (page - 1) * itemsPerPage;
  const end = page * itemsPerPage;
  const cardsToDisplay = products.slice(start, end);

  // Clear existing cards
  const cardContainer = document.getElementById("card-container");
  cardContainer.innerHTML = "";

  // Add new cards
  cardsToDisplay.forEach(product => {
    const card = document.createElement("div");
    card.classList.add("col-sm-6", "col-md-6", "col-lg-3", "mb-4");

    card.innerHTML = `
      <div class="card-content">
        <img src="${product.img}" alt="Product Image">
        <h5 class="mt-2">${product.title}</h5>
        <div class="price">${product.price}</div>
        <p class="description"style="  margin-bottom: 20%;">${product.description}</p>
        <span class="discount">34%</span>
        <div class="icons-overlay">
          <ul class="product-action">
            <li><a href="#"><i class="fa-regular fa-heart"></i></a></li>
            <li><a href="#"><i class="fa-solid fa-cart-arrow-down"></i></a></li>
            <li><a href="#"><i class="fa-solid fa-print"></i></a></li>
          </ul>
        </div>
      </div>
      `;
    cardContainer.appendChild(card);
  });
}

// Function to handle page change
function changeProductPage(page) {
  if (page < 1) page = 1;
  if (page > totalPages) page = totalPages;
  currentProductPage = page;
  displayCards(page);
}

// Add event listeners to pagination controls
document.getElementById("prev-page").addEventListener("click", function(e) {
  e.preventDefault();
  changeProductPage(currentProductPage - 1);
});

document.getElementById("next-page").addEventListener("click", function(e) {
  e.preventDefault();
  changeProductPage(currentProductPage + 1);
});

// Add event listeners to page number buttons
document.getElementById("page-1").addEventListener("click", function(e) {
  e.preventDefault();
  changeProductPage(1);
});
document.getElementById("page-2").addEventListener("click", function(e) {
  e.preventDefault();
  changeProductPage(2);
});
document.getElementById("page-3").addEventListener("click", function(e) {
  e.preventDefault();
  changeProductPage(3);
});

// Initial load
displayCards(currentProductPage);

